struct SpineNode {
    id: GrammarNodeId,
    left_child: GrammarNodeId,
}

// ============================================================
// Size computation — O(g), no tree expansion
// ============================================================

/// Compute subtree sizes (leaf count) for each grammar node.
/// Used by the full SCD algorithm for size-aware heavy-path detection.
/// Currently unused: spine detection relies only on ref_counts.
#[allow(dead_code)]
fn compute_sizes(
    node: GrammarNodeId,
    prod_map: &HashMap<GrammarNodeId, TslpRhs>,
    sizes: &mut HashMap<GrammarNodeId, u64>,
) -> u64 {
    if let Some(&s) = sizes.get(&node) {
        return s;
    }
    let size = match prod_map.get(&node) {
        Some(TslpRhs::Leaf(_)) | None => 1,
        Some(TslpRhs::Eml(l, r)) => {
            let ls = compute_sizes(*l, prod_map, sizes);
            let rs = compute_sizes(*r, prod_map, sizes);
            ls + rs
        }
    };
    sizes.insert(node, size);
    size
}


// ============================================================
// Depth computation for new grammar — O(g)
// ============================================================

fn compute_depths(
    prods: &[(GrammarNodeId, TslpRhs)],
    start: GrammarNodeId,
) -> HashMap<GrammarNodeId, usize> {
    let prod_map: HashMap<GrammarNodeId, &TslpRhs> =
        prods.iter().map(|(id, rhs)| (*id, rhs)).collect();
    let mut depths = HashMap::new();
    compute_depth_recursive(start, &prod_map, &mut depths);
    depths
}

fn compute_depth_recursive(
    node: GrammarNodeId,
    prod_map: &HashMap<GrammarNodeId, &TslpRhs>,
    depths: &mut HashMap<GrammarNodeId, usize>,
) -> usize {
    if let Some(&d) = depths.get(&node) {
        return d;
    }
    let depth = match prod_map.get(&node) {
        Some(TslpRhs::Leaf(_)) | None => 0,
        Some(TslpRhs::Eml(l, r)) => {
            let dl = compute_depth_recursive(*l, prod_map, depths);
            let dr = compute_depth_recursive(*r, prod_map, depths);
            1 + dl.max(dr)
        }
    };
    depths.insert(node, depth);
    depth
}

