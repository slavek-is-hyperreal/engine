#[cfg(test)]
mod tests {
    use super::*;
    use crate::compress::lift::lift_bytes;
    use crate::tslp::grammar::extract_grammar;

    #[test]
    fn test_ganardi_reduces_depth_no_oom() {
        // Right-spine tree of depth 63 (N=64 bytes lifted to EML)
        let data = vec![1u8; 64];
        let tree = lift_bytes(&data);
        let unbalanced_depth = tree.depth();

        let grammar = extract_grammar(&tree);
        let g_size_before = grammar.size();

        // Must not OOM — operates only on grammar productions
        let balanced = balance_grammar(&grammar);

        let balanced_depth = balanced.max_depth();
        println!("depth: {} → {}  grammar: {} → {}",
            unbalanced_depth, balanced_depth, g_size_before, balanced.size());

        // O(log N) depth — for N=64 expect <= log2(64) * 4 = 24
        assert!(balanced_depth < unbalanced_depth,
            "Balancing must reduce depth: {} → {}", unbalanced_depth, balanced_depth);
        assert!(balanced_depth <= 24,
            "Expected O(log N) depth, got {}", balanced_depth);

        // Grammar size grows by at most O(g log g) — reasonable bound
        assert!(balanced.size() <= g_size_before * 8,
            "Grammar blowup too large: {} → {}", g_size_before, balanced.size());
    }

    #[test]
    fn test_ganardi_large_spine() {
        // K=128 right-spine → should get O(log 128) = 7 depth
        let data = vec![42u8; 128];
        let tree = lift_bytes(&data);
        let grammar = extract_grammar(&tree);
        let balanced = balance_grammar(&grammar);

        println!("K=128: depth {} → {}  grammar size: {} → {}",
            tree.depth(), balanced.max_depth(), grammar.size(), balanced.size());

        assert!(balanced.max_depth() <= 20);
    }

    #[test]
    fn test_ganardi_already_balanced_no_blowup() {
        // If input is already balanced, grammar size should not grow much
        use crate::ast::*;
        use crate::nn_layer::build_dot_product_eml;
        let k = 16;
        let weights: Vec<f32> = (0..k).map(|i| (i + 1) as f32 * 0.1).collect();
        let inputs: Vec<_> = (0..k).map(|i| var(&format!("x{}", i))).collect();
        let tree = build_dot_product_eml(&inputs, &weights);  // already balanced
        let grammar = extract_grammar(&tree);
        let balanced = balance_grammar(&grammar);

        println!("already-balanced K={}: depth {} → {}  grammar {} → {}",
            k, grammar.max_depth(), balanced.max_depth(),
            grammar.size(), balanced.size());

        // Depth should not increase
        assert!(balanced.max_depth() <= grammar.max_depth() + 3,
            "Should not significantly increase depth on balanced input");
    }
}
